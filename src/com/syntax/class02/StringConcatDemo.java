package com.syntax.class02;

public class StringConcatDemo {

	public static void main(String[] args) {

		double price = 15.34;
		String fruit = "Orange";
		char symbol = '$';

		System.out.println("the price of " + fruit + " is " + symbol + price);

		float cost = 5f;
		String Book = "Book";
		char sym = '@';
		System.out.println("The price of " + Book + " is " + sym + cost);

	}

}
