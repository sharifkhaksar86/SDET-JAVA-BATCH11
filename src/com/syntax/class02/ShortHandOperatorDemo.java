package com.syntax.class02;

public class ShortHandOperatorDemo {

	public static void main(String[] args) {

		int number = 10;
		number++;
		System.out.println(number);

		number = 10;
		number += 30;
		System.out.println(number);

		number = 10;
		number -= 5;
		System.out.println(number);

		number = 10;
		number *= 5;
		System.out.println(number);
		number = 10;
		number /= 5;
		System.out.println(number);
		number = 10;
		number %= 5;
		System.out.println(number);

	}

}
