package com.syntax.class01;

public class DataTypes {

	public static void main(String[] args) {
		
		// representing and storing whole numbers
		byte box1=4;
		
		short box2=1000;
		
		int box3=1000000000;
		
		long box4=1000000000000000000L; // we should use letter L at the end to specify it is a long number
		
		// representing and storing Decimal Numbers
		
		float a=11.99f; // we should use letter f at the end to specify it is a Float
		
		double b=19.99; 
		
		
		// representing and storing Single Characters
		
		char letter='a'; //single quotation is used
		
		char character='#';
		
		
		// representing and storing boolean values (yes or no)
		
		boolean hungry=true;
		boolean tired=false;
		
		// we are storing values inside variables 
		// why there are warning? because we used the values but we didn't use it yet. 
		
		
	 System.out.println(box1);
	 System.out.println(box3);
	 System.out.println(tired);
	 // System.out.println(box); we can only access the variables that we created. 
	 
		
		
		
		
		
		
	}

}
